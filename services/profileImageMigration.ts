// Apply profile image URLs to an array of athletes and return the updated array
// This is called from App.tsx with the current data, then saved via normal save mechanism
export function applyProfileImages(athletes: Athlete[]): { athletes: Athlete[]; matched: number; notFound: string[] } {
    let matched = 0;
    const notFound: string[] = [];

    const updated = athletes.map(athlete => {
        const imageUrl = IMAGE_URL_MAP[athlete.user_name];
        if (imageUrl) {
            matched++;
            console.log(`âœ… Matched: ${athlete.user_name}`);
            return { ...athlete, profile_image_url: imageUrl };
        } else {
            notFound.push(athlete.user_name);
            return athlete;
        }
    });

    console.log(`\nðŸ“Š Migration complete: ${matched} matched, ${notFound.length} without images`);
    return { athletes: updated, matched, notFound };
}

